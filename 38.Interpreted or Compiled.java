// Java program showing that Java is both compiled and interpreted
class JavaTypeExample {

    public static void main(String[] args) {

        // Java source code is compiled into bytecode by the compiler
        System.out.println("Java is both compiled and interpreted.");

        // JVM executes (interprets/JIT compiles) the bytecode at runtime
        System.out.println("Compiled to bytecode, then executed by JVM.");
    }
}
