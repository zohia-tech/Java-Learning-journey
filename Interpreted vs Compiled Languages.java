// Java program explaining that Java is both compiled and interpreted
class CompiledVsInterpretedDemo {

    public static void main(String[] args) {

        // Java source code (.java) is compiled into bytecode (.class) by the compiler
        System.out.println("Java source code is first compiled to bytecode.");

        // JVM then interprets or executes the bytecode on the running system
        System.out.println("Then JVM interprets/runs that bytecode.");
    }
}
