public class WhileLoop { // Class name
    public static void main(String[] args) { // Main method starts here
        
        int i = 10; // Initialize variable with value 10
        
        do { 
            System.out.println(i); // Print the value of i
            i--; // Decrease i by 1
        } while (i > 10); // Condition is checked after executing the loop once
    }
}
