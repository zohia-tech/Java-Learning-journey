public class Main { // Main class where program starts
    public static void main(String[] args) { // Main method (entry point of program)

        System.out.println("Hello, World!"); // Prints Hello, World! on the screen

        int num1 = 6; // Stores first number
        int num2 = 5; // Stores second number
        int num3 = 7; // Stores third number

        int sum = num1 + num2 + num3; // Adds all three numbers and stores the result in sum

        System.out.println(sum); // Prints the final sum
    }
}
