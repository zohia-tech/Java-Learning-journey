public class CHW_04_Literals { // Class to demonstrate different types of literals
    public static void main(String[] args) { // Main method (program execution starts here)

        byte age = 34; // Stores a byte literal value
        char ch = 'A'; // Stores a character literal
        float f1 = 5.6f; // Stores a float literal (f is required for float)
        double d1 = 4.66; // Stores a double literal

        System.out.println(age); // Prints the value of age
    }
}
